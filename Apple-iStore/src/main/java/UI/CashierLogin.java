    private void bcountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bcountActionPerformed
        // TODO add your handling code here:
        if (jtproductname.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Fill out the product name");
        } else {
            Cashier cashier = new Cashier();

            List<Product> products = cashier.readProductsFromFile();

            int count = cashier.searchStockDetails(products, jtproductname.getText());
            if (count == 0) {
                JOptionPane.showMessageDialog(null, "Product not found.");
                lblcount.setText("0");
                jtproductname.setText("");
            } else {
                lblcount.setText(count + "");
                jtproductname.setText("");
            }

        }


    }//GEN-LAST:event_bcountActionPerformed

