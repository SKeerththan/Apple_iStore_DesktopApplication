    private void bfinddetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bfinddetailsActionPerformed
        // TODO add your handling code here:
        Cashier cashier = new Cashier();
        List<Product> products = cashier.readProductsFromFile();
        if (jtproductdata.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Fill out the product data");

        } else {
            if (jRadioButton1.isSelected() == true) {
                String response = cashier.searchProductDetailsBasedOnCategory(products, jtproductdata.getText());
                jTextArea1.setText(response);

            }

        }
        if (jRadioButton2.isSelected() == true) {
            String response = cashier.searchProductDetailsBasedOnName(products, jtproductdata.getText());
            jTextArea1.setText(response);
        }
        if (jRadioButton3.isSelected() == true) {
            String response = cashier.searchProductDetailsBasedOnPrice(products, jtproductdata.getText());
            jTextArea1.setText(response);
        }
    }//GEN-LAST:event_bfinddetailsActionPerformed

